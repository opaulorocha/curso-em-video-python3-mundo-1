n = input("Digite algo: ")
print("O tipo primitivo desse valor eh {}".format(type(n)))
print("Soh tem espacos? {}".format(n.isspace()))
print("Eh um numero? {}".format(n.isnumeric()))
print("Eh alfabetico? {}".format(n.isalpha()))
print("Eh alfanumerico? {}".format(n.isalnum()))
print("Esta em maisculas? {}".format(n.isupper()))
print("Esta em minusculas? {}".format(n.islower()))
print("Esta capitalizado? {}".format(n.istitle()))







